def neighbours(grid):
    kernel = np.array([[1, 1, 1],
                       [1, 0, 1],
                       [1, 1, 1]])
    return ss.convolve2d(grid, kernel, mode = 'same', boundary = 'wrap')


def determine_state(G):
    N = neighbours(G)
    G = np.where(((G == 1) & ((N == 3) | (N == 2))) | ((G == 0) & (N == 3)), 1, 0)
    return G

